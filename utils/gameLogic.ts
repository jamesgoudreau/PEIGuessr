// Haversine formula to calculate the distance in kilometers
export const calculateDistance = (point1: Coordinates, point2: Coordinates): number => {
  const R = 6371; // Radius of the Earth in km
  const dLat = (point2.lat - point1.lat) * Math.PI / 180;
  const dLng = (point2.lng - point1.lng) * Math.PI / 180;
  const a = 
    Math.sin(dLat/2) * Math.sin(dLat/2) +
    Math.cos(point1.lat * Math.PI / 180) * Math.cos(point2.lat * Math.PI / 180) * 
    Math.sin(dLng/2) * Math.sin(dLng/2);
  const c = 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1-a)); 
  return R * c;
};

// 5000 points maximum. PEI is small (about 224 km long)
export const calculateScore = (distanceKm: number): number => {
  const maxScore = 5000;
  const dropOffDistance = 150; 
  
  if (distanceKm <= 0.5) return maxScore;
  if (distanceKm >= dropOffDistance) return 0;
  
  // Exponential decay
  const score = maxScore * Math.exp(-distanceKm / 15);
  return Math.max(0, Math.floor(score));
};
